package com.example.gnitgnosil.myapplication;

public class Contacts {

    private String name;
    private String info;

    public Contacts(String name, String info) {
        this.name = name;
        this.info = info;
    }

    public String getName() {
        return name;
    }

    public String getInfo() {
        return info;
    }
}
